char	*ft_strlowcase(char *str)
{
	int	p;

	p = 0;
	while (str[p] != '\0')
	{
		if (str[p] >= 65 && str[p] <= 90)
		{
			str[p] += 32;
		}
		p++;
	}
	return (str);
}
